#!/usr/bin/env bash
# Shared instructions-file body for Sage platforms.
#
# Emits the canonical Sage process instructions (constitution, routing,
# workflow gates, learning triggers) to stdout. Used by all platform
# generators to produce the platform's instructions file
# (CLAUDE.md / AGENTS.md / GEMINI.md).
#
# The output is the same across platforms. Platform-specific differences
# (file name, paths, "Task tool" vs "sub-agent") are handled by the
# calling generator via sed post-processing on the output.
#
# Usage:
#   source runtime/platforms/_shared/instructions-body.sh
#   emit_instructions_body > /path/to/output.md
#
# The body contains the placeholder __CONSTITUTION_PLACEHOLDER__ which
# the calling generator replaces with merged constitution rules.

emit_instructions_body() {
  cat <<'INSTRUCTIONS_EOF'
# Sage — Project Instructions

> Auto-generated by Sage. Regenerate: `sage update`

Sage is an optional workflow and learning framework. It adds deterministic
state, declared capability ownership, verification gates, and durable recall
when the user explicitly selects it. It does not own ordinary conversation or
every task performed by the host agent.

## Runtime Contract

These rules apply while an explicit Sage command is being handled or a Sage
run is active. Outside an active run, the host agent may work normally and the
user may combine Sage with any compatible skill—or choose not to use Sage.

### Rule 0: Explicit Routes Are Authority

Use this authority order:

1. An explicit installed slash command starts, switches, or cancels a run.
2. Persisted active-run state continues that selected workflow.
3. Runtime advisory context may suggest an installed route, but the suggestion
   is optional and never creates state or arms a gate.
4. Plain task prose never starts, switches, cancels, or strictly gates a Sage
   workflow. Words such as "build", "map", or "fix" are not commands.

During an active run, ignore inferred rerouting until the user explicitly
switches or cancels. Reject stale or uninstalled route targets rather than
guessing a replacement.

**Composition:** Runtime composition context declares one owner and any
compatible helpers for each capability. Keep the selected owner through its
terminal. Helpers assist only in their declared roles. Never replace an owner
because another skill name or keyword appears in task prose. If multiple
exclusive owners remain valid, ask the user to choose instead of silently
selecting one.

**Execution topology:** Direct work, synchronous delegation, and durable Kanban
orchestration are different execution modes. Select them only through an exact
supported flag or explicit confirmation. Never choose a topology from words in
the task description, and never invent unavailable agent profiles.

**Strict enforcement:** Strict mode exists only for an explicit command that
includes `--strict` and has a healthy active strict run. Without an active
strict run, hooks fail open. During one, only declared shell, lane, ownership,
and lifecycle invariants may deny an operation; prose is not an authorization
or denial channel.

### Rule 1A: Deterministic Recall Without Double Injection

Use bounded recall context supplied by the configured recall owner. Exactly one
owner and backend may inject Sage learnings for a run. If another owner is
configured, Sage hooks stand down. Do not repeat mandatory searches in prose or
query a second memory system "just in case"; backend outages fail open.

### Workflow Gates (explicit or active Sage workflows only)

After an explicit route, read the installed command and canonical workflow for
detailed steps and capability references. These gates do not apply to ordinary
free input that has not selected Sage. If the command cannot be loaded, fail
open and explain that the installed Sage surface needs regeneration.

**Build (Standard+ scope) — FILE CHECKS:**
BEFORE implementing, verify BOTH files exist on disk:
  .sage/work/[initiative]/spec.md — with status: completed
  .sage/work/[initiative]/plan.md — with status: completed
If EITHER file is missing → create it first. No exceptions.

**Branch gate (Standard+ scope, git projects):**
BEFORE implementation commits begin, verify HEAD is not the default
branch — unless a branching decline is recorded in the initiative's
decision log. If HEAD is on the default branch with no recorded
decline: propose the initiative branch (`feat/`, `fix/`, or
`arch/` per workflow),
confirm with the user, create it. The full protocol lives in
`sage/core/capabilities/execution/git-discipline/SKILL.md`; this
gate carries only the observable check. Merging is ALWAYS a
user-gated [M] menu action — no workflow path merges on its own.
Under `isolation: worktree` in `.sage/config.yaml`, a Standard+
workflow in the main checkout offers the worktree bounce
(`sage worktree <slug>`) before branching in place; `isolation: branch`
(default) branches in place. Not a git repository → this gate does
not exist.

Do NOT rationalize skipping:
- "The design is clear from previous discussion" → NOT a spec file
- "The user described what they want" → NOT a spec file
- "This is straightforward" → if Standard scope, spec required
- "Just build it" → write a minimal 5-line spec, get [A]/[R]

Gate sequence:
1. Spec file to .sage/work/ → present [A]/[R] → wait for approval
2. Plan file to .sage/work/ → present [A]/[R] → wait for approval
3. Implement (tests before code, via build-loop)
4. Verify with PASTED test output → present [A]/[R]

**Fix — SCOPE AFTER ROOT CAUSE:**
1. Investigate root cause with evidence → present [A]/[R]/[S] → wait
2. SCOPE THE FIX after root cause is confirmed:
   Surgical (1-2 files) → proceed to fix
   Moderate (3-5 files) → write fix plan first → [A]/[R]
   Systemic (5+ files, interface changes) → ESCALATE to /build or /architect
3. Implement fix → verify with PASTED test output → [A]/[R]
DO NOT fix before root cause is confirmed.
DO NOT skip fix scoping — a "quick fix" that touches 8 files is a rebuild.

**Architect — FILE CHECKS:**
1. Complete elicitation (vision, constraints, gaps) — all 3 rounds.
   Each round produces visible output. brief.md MUST EXIST before design.
   Do NOT compress rounds. Do NOT skip because "I understand the system."
2. Design with ADRs to .sage/docs/ → spec.md to .sage/work/ → [A]/[R]
3. Milestone plan → [A]/[R] → phased build
   Each milestone follows build workflow gates independently.
DO NOT design before brief.md exists.
DO NOT batch-implement milestones without per-milestone checkpoints.

### Rule 1: State First

When a Sage run is active, use its persisted run state and scan the matching
`.sage/work/` frontmatter before continuing. Read the active initiative's
decision log when it exists. Do not create or switch state for an advisory
route.

**Compliance:** Active work is acknowledged before new work begins.

### Rule 2: Skills Before Assumptions

For a selected capability, read and follow its resolved owner skill. Skills are
in `sage/skills/` and may be composed with compatible external skills. Do not
load every Sage skill or replace the resolved owner with a merely similar one.

**Compliance:** Skill is read before producing skill-covered output.

### Rule 3: Document Decisions

When the selected workflow declares a durable artifact, record project
decisions in `.sage/work/` or `.sage/docs/`. Ordinary work outside Sage does not
gain an artifact requirement merely because a route was suggested.

**Compliance:** Declared Sage artifacts are durable and replayable.

### Rule 4: Checkpoints Are Sacred

Honor approval points declared by the selected workflow. Do not add approval
gates to unrelated work, and do not infer approval from silence. Never change
an approved scope unilaterally or mark a run complete while declared work
remains unfinished.

**Compliance:** Each approval gate presents work and waits for response.
Scope changes require explicit user consent.

### Rule 5: Verify Before Claiming Done

Before claiming an active Sage run complete, obtain fresh verification in
proportion to risk and compare the result with declared acceptance criteria.
Preserve the actual evidence in run state or the workflow artifact. A concise
user-facing summary is allowed; a passing claim without fresh evidence is not.

### Rule 6: Capture Corrections

Hooks may record correction, failure, and recovery evidence as a pending
candidate. Evidence detection is not permission to author a learning. Invoke
the canonical `sage-self-learning` skill to interpret the evidence, search before store,
enrich an equivalent record, or invalidate and link a corrected
record. Persist through the configured backend only.

**Compliance:** Candidate delivery is exactly once; storage remains owned by
the canonical skill and configured backend.

### Rule 7: Record Decisions at Checkpoints

At each checkpoint, **prepend** significant decisions to the
**initiative's decision log** —
`.sage/work/[initiative]/decisions.md` (insert after the
`# Decisions` header, before existing entries) — newest first.
Record what was decided, why, and what alternatives were
considered. Writers switch immediately, including for initiatives
already in flight; readers check the initiative log first and fall
back to the global file, so the switch is safe.

The **global** `.sage/decisions.md` remains for cross-initiative
decisions only (constitution choices, conventions, project-wide
calls). Two parallel initiative branches prepending to one global
file is a guaranteed merge conflict — the per-initiative split is
what makes parallel sessions safe.

**Archive rotation:** When the global decisions.md exceeds ~200
lines, at the next workflow close: keep the 20 most recent entries,
move the rest to `decisions-{YYYY-MM-DD}.md`. Archives are
read-only reference. (Initiative logs live with their work dir —
no rotation.)

The file system — what artifacts exist in `.sage/work/` and their
frontmatter — is the source of truth for state. Decision logs are
the source of truth for reasoning and context.

**Compliance:** the active initiative's decisions.md has a new
entry (prepended) after each checkpoint that involved a decision.

