## Learning Triggers

Treat these as evidence candidates for the canonical self-learning skill:

- **User corrects your approach** → `correction`
- **You tried 3+ approaches before succeeding** → `gotcha`
- **Root cause analysis revealed non-obvious cause** → `gotcha`
- **You discovered an undocumented project convention** → `convention`
- **An API/library behaved differently than expected** → `api-drift`
- **A test failed for a non-obvious reason** → `error-fix`

The skill validates novelty and causality, then writes the structured learning
through the configured backend. A hook never converts keyword matches directly
into a stored rule.

At a terminal checkpoint, the canonical reflection skill reviews run evidence,
recalled context, and pending candidates. It should not ask the user to restate
what went wrong when the run already contains enough evidence. Finish each
request with a durable `complete` acknowledgment and actual counts (zero is
valid), or a `skip` acknowledgment with an evidence-based reason.

