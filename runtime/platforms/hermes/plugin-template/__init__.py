def _run_one_gate(gate: str, required: str, args: dict, cwd: str) -> dict:
    is_mandatory = required == "mandatory"

    if gate == "spec-compliance":
        plan_file = str(args.get("plan_file") or "").strip()
        task_number = str(args.get("task_number") or "").strip()
        if not plan_file or not task_number:
            return _gate_result(
                gate,
                required,
                "missing_args",
                is_mandatory,
                {"error": "plan_file and task_number are required for spec-compliance"},
            )
        data = _run_script_data("sage-spec-check.sh", [plan_file, task_number], cwd)
    elif gate == "hallucination-check":
        target = str(args.get("target") or ".").strip()
        project_root = str(args.get("project_root") or ".").strip()
        data = _run_script_data("sage-hallucination-check.sh", [target, project_root], cwd)
    elif gate == "verification":
        project_root = str(args.get("project_root") or ".").strip()
        data = _run_script_data("sage-verify.sh", [project_root], cwd, timeout=600)
    elif gate == "visual-verification":
        url = str(args.get("visual_url") or args.get("url") or "").strip()
        if not url:
            return _gate_result(
                gate,
                required,
                "missing_args",
                is_mandatory,
                {"error": "visual_url is required for visual-verification"},
            )
        output_dir = str(args.get("visual_output_dir") or args.get("output_dir") or ".sage/screenshots").strip()
        data = _run_script_data("sage-visual-gate.sh", [url, output_dir], cwd, timeout=600)
    elif gate in AGENT_REVIEW_GATES:
        return _gate_result(
            gate,
            required,
            "agent_review_required",
            False,
            {"agent_review_required": True, "note": AGENT_REVIEW_GATES[gate]},
        )
    else:
        return _gate_result(gate, required, "unsupported", is_mandatory, {"error": "No Hermes gate implementation registered"})

    return _gate_result(
        gate,
        required,
        "script_passed" if data.get("ok") else "failed",
        is_mandatory and not data.get("ok"),
        {
            **data,
            "agent_review_required": bool(data.get("ok")),
            "review_note": SCRIPT_REVIEW_NOTES.get(gate),
        },
    )


def _handle_sage_run_gates(args: dict, **_kwargs) -> str:
    mode = _canonical_gate(args.get("mode") or "")
    if mode not in DEFAULT_GATE_MODES:
        return json.dumps({"ok": False, "error": "mode must be one of: fix, build, architect"})

    cwd = _cwd_from_args(args)
    config, meta = _mode_config(cwd, mode)
    optional_requested = set(_as_list(args.get("optional_gates")))
    optional_requested.update(meta.get("optional_enabled", []))
    include_optional = bool(args.get("include_optional"))

    mandatory = config["mandatory"]
    optional = config["optional"]
    skipped = set(config["skipped"])
    active_optional = [
        gate for gate in optional
        if include_optional or gate in optional_requested
    ]
    visual_url = str(args.get("visual_url") or args.get("url") or "").strip()
    if visual_url and "visual-verification" not in skipped:
        active_optional.append("visual-verification")

    active = _sort_gates([gate for gate in mandatory + active_optional if gate not in skipped])
    required_by_gate = {gate: "mandatory" for gate in mandatory if gate not in skipped}
    for gate in active_optional:
        if gate not in required_by_gate:
            required_by_gate[gate] = "optional"

    results = [_run_one_gate(gate, required_by_gate.get(gate, "optional"), args, cwd) for gate in active]
    optional_not_run = _sort_gates([
        gate for gate in optional
        if gate not in skipped and gate not in active
    ])
    blocking = [result for result in results if result.get("blocking")]
    review_required = [result for result in results if result.get("agent_review_required")]
    waiver_required = meta.get("waiver_required") or []
    ok = not blocking and not waiver_required

    return json.dumps({
        "ok": ok,
        "all_gates_complete": ok and not review_required,
        "mode": mode,
        "cwd": cwd,
        "config": config,
        "sources": meta,
        "active_gates": active,
        "optional_not_run": optional_not_run,
        "skipped": _sort_gates(list(skipped)),
        "waiver_required": waiver_required,
        "agent_review_required": [
            {
                "gate": result.get("gate"),
                "required": result.get("required"),
                "note": result.get("review_note") or result.get("note"),
            }
            for result in review_required
        ],
        "results": results,
        "summary": {
            "script_passed": sum(1 for result in results if result.get("status") == "script_passed"),
            "failed": sum(1 for result in results if result.get("status") == "failed"),
            "agent_review_required": len(review_required),
            "missing_args": sum(1 for result in results if result.get("status") == "missing_args"),
            "unsupported": sum(1 for result in results if result.get("status") == "unsupported"),
            "blocking": len(blocking) + len(waiver_required),
        },
    })


