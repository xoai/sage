def register(ctx) -> None:
    """Hermes plugin entry point."""
    if not SKILLS_DIR.is_dir():
        logger.warning("Sage skills directory not found: %s", SKILLS_DIR)
        return

    for child in sorted(SKILLS_DIR.iterdir()):
        skill_md = child / "SKILL.md"
        if not child.is_dir() or not skill_md.exists():
            continue
        try:
            ctx.register_skill(child.name, skill_md)
        except Exception as exc:
            logger.debug("Could not register skill %s: %s", child.name, exc)

    for command_name, skill_id in SLASH_COMMAND_SKILLS:
        if not _skill_path(skill_id).exists():
            continue
        _register_command(ctx, command_name, skill_id)

    for tool_name, schema, handler, emoji in SAGE_TOOLS:
        try:
            ctx.register_tool(
                name=tool_name,
                toolset="sage",
                schema=schema,
                handler=handler,
                emoji=emoji,
            )
        except Exception as exc:
            logger.debug("Could not register tool %s: %s", tool_name, exc)
