def _skill_path(skill_id: str) -> Path:
    return SKILLS_DIR / skill_id / "SKILL.md"


def _read_skill_text(skill_id: str, raw_args: str = "") -> str:
    path = _skill_path(skill_id)
    try:
        text = path.read_text(encoding="utf-8")
    except FileNotFoundError:
        return f"Sage workflow file not found: {path}"
    except OSError as exc:
        return f"Could not read Sage workflow {skill_id}: {exc}"
    text = text.replace("$ARGUMENTS", raw_args or "")
    if _needs_delegation_note(text):
        text = f"{text.rstrip()}\n{HERMES_DELEGATION_NOTE}"
    if skill_id in {"build", "fix", "architect"}:
        text = f"{text.rstrip()}\n{HERMES_GATE_TOOL_NOTE}"
    return text


def _needs_delegation_note(text: str) -> bool:
    lowered = text.lower()
    triggers = (
        "task tool",
        "sub-agent",
        "subagent",
        "independent review",
        "auto-review",
        "auto-qa",
        "gate 3",
        "delegate",
    )
    return any(trigger in lowered for trigger in triggers)


def _read_skill_description(skill_id: str) -> Optional[str]:
    path = _skill_path(skill_id)
    try:
        text = path.read_text(encoding="utf-8")
    except OSError:
        return None
    if not text.startswith("---"):
        return None
    end = text.find("\n---", 3)
    if end < 0:
        return None
    frontmatter = text[3:end]
    in_description = False
    for line in frontmatter.splitlines():
        if line.startswith("description:"):
            value = line.split(":", 1)[1].strip().strip("'\"")
            if value in {">", ">-", "|", "|-"}:
                in_description = True
                continue
            return value or None
        if in_description:
            stripped = line.strip().strip("'\"")
            if stripped:
                return stripped
    return None


def _register_command(ctx, command_name: str, skill_id: str) -> None:
    def handler(raw_args: str = "") -> str:
        return _read_skill_text(skill_id, raw_args)

    description = _read_skill_description(skill_id) or f"Sage {skill_id} workflow"
    try:
        ctx.register_command(
            command_name,
            handler=handler,
            description=description,
        )
    except TypeError:
        ctx.register_command(command_name, handler, description)
    except Exception as exc:
        logger.debug("Could not register /%s: %s", command_name, exc)


def _cwd_from_args(args: dict) -> str:
    raw = str(args.get("cwd") or os.environ.get("TERMINAL_CWD") or os.getcwd()).strip()
    return raw or os.getcwd()


def _run_script_data(script_name: str, argv: list[str], cwd: str, timeout: int = 300) -> dict:
    script = HOOKS_DIR / script_name
    if not script.exists():
        return {"ok": False, "error": f"Sage script not found: {script}", "script": script_name, "cwd": cwd}
    completed = subprocess.run(
        ["bash", script.as_posix(), *argv],
        cwd=cwd,
        capture_output=True,
        text=True,
        timeout=timeout,
    )
    stdout = completed.stdout or ""
    stderr = completed.stderr or ""
    return {
        "ok": completed.returncode == 0,
        "exit_code": completed.returncode,
        "script": script_name,
        "cwd": cwd,
        "stdout": stdout[-20000:],
        "stderr": stderr[-8000:],
    }


def _run_script(script_name: str, argv: list[str], cwd: str, timeout: int = 300) -> str:
    return json.dumps(_run_script_data(script_name, argv, cwd, timeout))


def _load_yaml(path: Path) -> dict:
    if not path.exists():
        return {}
    try:
        import yaml  # type: ignore

        data = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
        return data if isinstance(data, dict) else {}
    except Exception:
        return _parse_simple_gate_yaml(path.read_text(encoding="utf-8"))


def _parse_simple_gate_yaml(text: str) -> dict:
    data: dict[str, dict[str, list[str]]] = {}
    section: str | None = None
    key: str | None = None
    for raw in text.splitlines():
        line = raw.split("#", 1)[0].rstrip()
        if not line.strip():
            continue
        indent = len(raw) - len(raw.lstrip(" "))
        stripped = line.strip()
        if indent == 0 and stripped.endswith(":"):
            section = stripped[:-1]
            data.setdefault(section, {})
            key = None
        elif indent == 2 and stripped.endswith(":") and section:
            key = stripped[:-1]
            data[section].setdefault(key, [])
        elif stripped.startswith("- ") and section and key:
            data[section].setdefault(key, []).append(stripped[2:].strip())
    return data


def _as_list(value) -> list[str]:
    if value is None:
        return []
    if isinstance(value, list):
        raw = value
    else:
        raw = [value]
    return [_canonical_gate(item) for item in raw if str(item).strip()]


def _canonical_gate(value) -> str:
    name = str(value).strip().lower().replace("_", "-")
    return GATE_ALIASES.get(name, name)


def _dedupe(items: list[str]) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for item in items:
        if item and item not in seen:
            seen.add(item)
            out.append(item)
    return out


def _sort_gates(items: list[str]) -> list[str]:
    return sorted(_dedupe(items), key=lambda gate: (GATE_ORDER.get(gate, 99), gate))


def _gate_modes_path(cwd: str) -> Path:
    project_modes = Path(cwd) / ".sage" / "gates" / "gate-modes.yaml"
    if project_modes.exists():
        return project_modes
    return HOOKS_DIR / "gate-modes.yaml"


def _mode_config(cwd: str, mode: str) -> tuple[dict, dict]:
    source = _gate_modes_path(cwd)
    modes = _load_yaml(source)
    raw = modes.get(mode) if isinstance(modes, dict) else None
    if not isinstance(raw, dict):
        raw = DEFAULT_GATE_MODES.get(mode, {})
        source_label = "built-in fallback"
    else:
        source_label = str(source)

    config = {
        "mandatory": _as_list(raw.get("mandatory")),
        "optional": _as_list(raw.get("optional")),
        "skipped": _as_list(raw.get("skipped")),
    }
    meta = {
        "mode_config": source_label,
        "project_config": None,
        "waiver_required": [],
        "optional_enabled": [],
    }

    project_config = Path(cwd) / ".sage" / "config.yaml"
    project_data = _load_yaml(project_config)
    gates = project_data.get("gates") if isinstance(project_data, dict) else None
    if isinstance(gates, dict):
        meta["project_config"] = str(project_config)
        override = None
        if isinstance(gates.get("modes"), dict) and isinstance(gates["modes"].get(mode), dict):
            override = gates["modes"][mode]
        elif isinstance(gates.get(mode), dict):
            override = gates[mode]
        elif any(key in gates for key in ("mandatory", "optional", "skipped")):
            override = gates

        if isinstance(override, dict):
            for key in ("mandatory", "optional", "skipped"):
                if key in override:
                    config[key] = _as_list(override.get(key))

        disabled = _as_list(gates.get("disabled"))
        if disabled:
            original_mandatory = set(config["mandatory"])
            config["mandatory"] = [gate for gate in config["mandatory"] if gate not in disabled]
            config["optional"] = [gate for gate in config["optional"] if gate not in disabled]
            config["skipped"] = _dedupe(config["skipped"] + disabled)
            meta["waiver_required"] = sorted(original_mandatory.intersection(disabled))

        additional = _as_list(gates.get("additional"))
        if additional:
            config["optional"] = _dedupe(config["optional"] + additional)

        enabled_optional = (
            gates.get("enabled")
            or gates.get("optional_enabled")
            or gates.get("run_optional")
            or gates.get("enabled_optional")
        )
        meta["optional_enabled"] = _as_list(enabled_optional)

    for key in ("mandatory", "optional", "skipped"):
        config[key] = _sort_gates(config[key])
    return config, meta


def _gate_result(gate: str, required: str, status: str, blocking: bool, details: dict | None = None) -> dict:
    result = {
        "gate": gate,
        "required": required,
        "status": status,
        "blocking": blocking,
    }
    if details:
        result.update(details)
    return result


def _handle_sage_spec_check(args: dict, **_kwargs) -> str:
    plan_file = str(args.get("plan_file") or "").strip()
    task_number = str(args.get("task_number") or "").strip()
    if not plan_file or not task_number:
        return json.dumps({"ok": False, "error": "plan_file and task_number are required"})
    return _run_script("sage-spec-check.sh", [plan_file, task_number], _cwd_from_args(args))


