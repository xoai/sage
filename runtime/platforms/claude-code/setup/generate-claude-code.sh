#!/usr/bin/env bash
# ═══════════════════════════════════════════════════════════════
# Sage → Claude Code Setup
# Generates CLAUDE.md + .claude/commands/ from Sage core
# ═══════════════════════════════════════════════════════════════
set -euo pipefail

SAGE_ROOT="${1:-.}"
SAGE_DIR="$SAGE_ROOT/sage"
CLAUDE_DIR="$SAGE_ROOT/.claude"
PROJECT_SAGE="$SAGE_ROOT/.sage"
CORE="$SAGE_DIR/core"

echo ""
echo "🚀 Sage → Claude Code Setup"
echo "═══════════════════════════════"

# ── Validate ──
if [ ! -d "$CORE" ]; then
  echo "❌ Sage framework not found at $SAGE_DIR"
  echo "   Run this from the project root where sage/ is located."
  exit 1
fi

# ── Create .claude structure ──
echo ""
echo "📁 Creating .claude/ structure..."
mkdir -p "$CLAUDE_DIR/commands"

# ═══════════════════════════════════════════════════════════════
# CLAUDE.md — Generated from canonical template pattern
# Follows context-loader strategy: Layer 1 (always-on) only
# ═══════════════════════════════════════════════════════════════
echo "📝 Generating CLAUDE.md..."
cat > "$SAGE_ROOT/CLAUDE.md" << 'CLAUDEEOF'
# Sage — Project Instructions

> Auto-generated by Sage. Regenerate: `sage update`

## Process Constitution (Non-Negotiable)

These rules apply to EVERY response. No exceptions.

1. **State first.** Before ANY substantial response, read `.sage/progress.md`. If work is in progress, acknowledge it and propose resuming.

2. **Skills before assumptions.** If a Sage skill is relevant to the current task, read and follow it. Do not rely on general training when a specific skill exists. Sage skills are in `sage/skills/` — they are tested, refined processes that produce better outcomes.

3. **Use Sage's planning, not your default.** Do NOT generate implementation plans, specs, or task breakdowns using default behavior. Use Sage's planning process — it saves persistent artifacts to `.sage/` that survive across sessions.

4. **Checkpoints are sacred.** Never skip human approval on briefs, specs, plans, or final deliverables. Show the work. Wait for approval.

5. **Save state.** After every significant step, update `.sage/progress.md`.

## How You Work

You are augmented by Sage — an intelligent skills framework for AI agents. Sage provides skills that cover the full spectrum from understanding users to shipping code.

**For ANY substantial request** (build, create, analyze, research, redesign, plan, fix, improve):
→ Read the sage-navigator at `sage/core/capabilities/orchestration/sage-navigator/SKILL.md`
→ Follow its process: assess intent, detect gaps, recommend the right workflow
→ This produces better outcomes than ad-hoc approaches

**For quick questions or simple tasks:**
→ Respond directly — not everything needs a workflow

## Commands

Use slash commands for direct access to workflows:

| Command | What It Does |
|---------|-------------|
| `/project:sage` | **Start here.** Reads project state, assesses intent, guides the process |
| `/project:build` | Feature development: scope → spec → plan → implement |
| `/project:fix` | Quick debug → test → fix → verify |
| `/project:architect` | System design: deep elicitation → architecture → phased build |
| `/project:status` | Check current project state |

## How to Communicate

At every step, make it easy for the user to respond:
- **Decision points:** Present 2-4 numbered options + a free-form escape.
- **Checkpoints:** Use `[A] Approve` / `[R] Revise` shortcuts.
- **Continuations:** Recommend the next step with `[C] Continue`.
Always accept free-form input — these patterns guide, they don't constrain.

## Available Skills

Sage skills are in `sage/skills/`. Current skills cover:
- **Discovery:** user research, needs analysis, opportunity mapping
- **Design:** evaluation, briefs, voice & tone, heuristic review
- **Engineering:** specifications, planning, implementation, review

Custom skills can be added to `sage/skills/@custom/`.
Read a skill's SKILL.md to activate its methodology.

## Project State

All Sage state lives in `.sage/`:
- `progress.md` — session continuity (what's done, what's next)
- `journal.md` — artifact index and change log
- `docs/` — project-level knowledge (analyses, decisions, guides)
- `work/` — per-initiative deliverables (brief, spec, plan per initiative)
CLAUDEEOF
echo "  ✓ CLAUDE.md"

# ═══════════════════════════════════════════════════════════════
# Commands — Adapted from core/workflows/ for Claude Code
# Path substitution: generic refs → sage/core/... paths
# Adds $ARGUMENTS for Claude Code command system
# ═══════════════════════════════════════════════════════════════
echo ""
echo "📎 Generating .claude/commands/ from core workflows..."

for wf in "$CORE"/workflows/*.workflow.md; do
  [ -f "$wf" ] || continue
  basename_wf=$(basename "$wf" .workflow.md)

  # Adapt: strip frontmatter, substitute refs with Claude Code paths, add $ARGUMENTS
  {
    sed '/^---$/,/^---$/d' "$wf" \
      | sed 's|\*\*sage-navigator\*\* skill|**sage-navigator** skill at `sage/core/capabilities/orchestration/sage-navigator/SKILL.md`|g' \
      | sed "s|sage-navigator's intelligence layer|sage-navigator's intelligence layer (\`sage/core/capabilities/orchestration/sage-navigator/SKILL.md\`, section 2)|g" \
      | sed 's|If relevant Sage skills exist, read and follow them.|If relevant Sage skills exist in `sage/skills/`, read and follow them.|g' \
      | sed '/^$/N;/^\n$/d'
    echo ""
    echo '$ARGUMENTS'
  } > "$CLAUDE_DIR/commands/${basename_wf}.md"

  echo "  ✓ ${basename_wf}.md → /project:${basename_wf}"
done

# ═══════════════════════════════════════════════════════════════
# Project state — .sage/ initialization
# ═══════════════════════════════════════════════════════════════
echo ""
echo "📊 Checking project state..."
if [ -d "$PROJECT_SAGE" ]; then
  echo "  ✓ .sage/ already exists"
else
  mkdir -p "$PROJECT_SAGE/work" "$PROJECT_SAGE/docs"

  cat > "$PROJECT_SAGE/progress.md" << PROGEOF
# Progress

Mode: idle
Feature: none
Phase: ready
Next: Describe what you want to build — Sage will guide you
Updated: $(date -Iseconds)
PROGEOF

  cat > "$PROJECT_SAGE/journal.md" << 'JRNEOF'
# Project Journal

## Artifact Index

| Artifact | Status | Path | Updated |
|----------|--------|------|---------|
| (none yet) | — | — | — |

## Change Log

- [init] Sage initialized
JRNEOF

  cat > "$PROJECT_SAGE/conventions.md" << 'CONVEOF'
# Project Conventions

Discovered by Sage on first run.
The codebase-scan capability will enrich this automatically.
CONVEOF

  echo "  ✓ .sage/ initialized"
fi

# ═══════════════════════════════════════════════════════════════
# Summary
# ═══════════════════════════════════════════════════════════════
echo ""
echo "═══════════════════════════════"
echo "✅ Sage → Claude Code setup complete"
echo ""
CMD_COUNT=$(find "$CLAUDE_DIR/commands" -name "*.md" 2>/dev/null | wc -l)
echo "  CLAUDE.md            → always-on project instructions"
echo "  .claude/commands/    → $CMD_COUNT slash commands"
echo "  .sage/               → project state directory"
echo ""
echo "Next steps:"
echo "  1. Open this project in Claude Code"
echo "  2. Type /project:sage and describe what you want to build"
echo "  3. Type /project:status to check project state"
echo ""
